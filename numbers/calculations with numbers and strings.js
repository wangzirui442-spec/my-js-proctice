//string and number type coercion
console.log(5 + "5");  //"55"   string
console.log("5" + 5);   //"55"    string
console.log("10" - 2);    //8     number
console.log("10" * 2);    //20     number
console.log("abc" - 1);    //NaN    number
// bolean/mull / undefined operators
console.log(true + 1);    //2    number
console.log("Hello" + true);   // "Hellotrue"   string
console.log(false + 5);       //5   number
console.log(null +6 );    //6    number
console.log(undefined + 199);    // NaN    number
